def _normalize_database_url(raw_value: str) -> str:
    value = (raw_value or "").strip()
    if not value:
        return ""

    if value.lower().startswith("psql "):
        value = value[5:].strip()

    if len(value) >= 2 and value[0] == value[-1] and value[0] in {"'", '"'}:
        value = value[1:-1].strip()

    return value


